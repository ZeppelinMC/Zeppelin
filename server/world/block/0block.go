func atoi(str string) int {
	v, _ := strconv.Atoi(str)

	return v
}

// A usable block is a block which performs a certain action when left clicked
type Usable interface {
	Block
	Use(clicker any, pk play.UseItemOn, dimension *dimension.Dimension)
}

// A block state haver is one that also has a block state
type BlockEntityHaver interface {
	Block
	BlockEntity(pos.BlockPosition) chunk.BlockEntity
}

// A block custom place sound haver is one that has a custom place sound
type BlockCustomPlaceSoundHaver interface {
	Block
	PlaceSound(pos.BlockPosition) *play.SoundEffect
}

type BreakInfo struct {
	// The hardness of this block
	Hardness float32
	// Whether this block drops xp when broken
	DropsXp bool
	// The xp points this block can drop
	MinXp, MaxXp float32

	// The blast resistance of this block
	BlastResistance float32
}

// Is checks if the tag applies to the block
func Is(block Block, tagName string) bool {
	name, _ := block.Encode()

	return slices.Index(tags.Tags.Tags["minecraft:block"][tagName], registry.Block.Get(name)) != -1
}

// A block that can be broken
type Breakable interface {
	BreakInfo() BreakInfo
}

